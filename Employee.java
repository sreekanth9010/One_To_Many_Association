package one_to_many_associations;


//dependent class
public class Employee {

	private int eId;
	private String eName;
	
	//getters and setters
	public int geteId() {
		return eId;
	}
	public void seteId(int eId) {
		this.eId = eId;
	}
	public String geteName() {
		return eName;
	}
	public void seteName(String eName) {
		this.eName = eName;
	}
	
	
}
